#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	char	*cp_dst;
	char	*cp_src;

	cp_dst = (char *)dst;
	cp_src = (char *)src;
	if (!dst && !src)
		return (dst);
	while (n--)
		*(cp_dst++) = *(cp_src++);
	return (dst);
}
